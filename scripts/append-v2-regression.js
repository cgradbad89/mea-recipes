#!/usr/bin/env node
// Appends a "Regression check vs v1" section to batch4-canonical-dryrun-v2.md (READ-ONLY repo doc).
const fs = require('fs')
const path = require('path')
const v1 = require('./canonical-dryrun-v1-raw.json')
const v2 = require('./canonical-dryrun-v2-raw.json')
const m1 = {}; v1.diffs.forEach(d => { m1[d.title] = d })

const changed = []
for (const b of v2.diffs) {
  const a = m1[b.title]; if (!a || a.error || b.error) continue
  const dc = b.proposed.total.calories - a.proposed.total.calories
  if (Math.abs(dc) >= 1) changed.push({ t: b.title, dc, v1: a.proposed.total.calories, v2: b.proposed.total.calories })
}
changed.sort((x, y) => Math.abs(y.dc) - Math.abs(x.dc))

function reason(t) {
  const s = t.toLowerCase()
  if (/chili con carne|pulled pork|carnitas|brisket|bulgogi|pepper steak|posole|fried rice/.test(s)) return 'ground-meat hijack removed → fuzzy cut'
  if (/paprikash/.test(s)) return 'pork lard → fuzzy (salt pork)'
  if (/minnesota pork chop/.test(s)) return 'cream-of-mushroom → fuzzy condensed soup'
  if (/grilled salmon/.test(s)) return '"in half" no longer → cream'
  if (/noodle|soba|ramen|gochujang/.test(s)) return 'bare "noodles" alias dropped → fuzzy'
  if (/pot roast/.test(s)) return 'beef gravy mix → fuzzy gravy'
  if (/chili/.test(s)) return 'lost a hijacked-meat/tomato hit → fuzzy'
  return 'lost a v1 canonical hit → fuzzy (baseline)'
}

const L = []
L.push('')
L.push('## Regression check vs v1 (Batch-4-fix verification)')
L.push('')
L.push('Generated by `scripts/diff-v1-v2.js` comparing every canonical resolution in v1')
L.push('(commit 64a45c0) vs this v2. Only the alias/guard layer changed; the matcher logic,')
L.push('FDC IDs, and per-100g macros are identical.')
L.push('')
L.push('**The 16 re-audit regressions — all resolved:**')
L.push('- **11 ground-meat hijacks** (beef brisket/chuck/ribeye/flank, pork shoulder/butt/lard, beef gravy mix, fried-rice pork): the bare `{beef}`/`{pork}` aliases (from "minced beef" etc.) were dropped, so these **fall through to the fuzzy matcher** that already had the correct specific cut. Removes 480–2,359 phantom kcal/recipe (e.g. Texas Chili 5202→2843, BBQ Pulled Pork 5198→3112).')
L.push('- **4 half-and-half cream hijacks** ("…in half" / "half-moons" prep fragments): the `half and half` entry was removed → "horizontally in half" & "half-moons" fall through; Mediterranean Grilled Salmon\'s "tomato/lemon …in half" now resolve to **tomato/lemon** (the actual food), not cream.')
L.push('- **1 cream-of-mushroom hijack**: the `mushroom` guard now vetoes soup/cream → Minnesota Pork Chop Casserole\'s "cream of mushroom soup" falls through to the baseline\'s correct condensed-soup match (2689→3028 kcal — the under-count corrected upward).')
L.push('')
L.push('**No new regressions.** Diff of canonical hits v1→v2: **0 recipes gained a wrong canonical match.** The only gains were 2 negligible herb lines ("oregano basil"→oregano, "parsley basil"→parsley, ~1–5 g). The interim tomato tie-break (plain "tomatoes"→fresh) was eliminated by dropping the bare tomato aliases, so unqualified/canned tomatoes again fall through to the fuzzy matcher exactly as in v1.')
L.push('')
L.push(`**All ${changed.length} recipes whose total changed v1→v2** (each is an intended fix or fall-through to the existing matcher — none worse):`)
L.push('')
L.push('| recipe | v1 kcal | v2 kcal | Δ | reason |')
L.push('|---|---|---|---|---|')
for (const r of changed) L.push(`| ${r.t} | ${r.v1} | ${r.v2} | ${r.dc > 0 ? '+' : ''}${r.dc} | ${reason(r.t)} |`)
L.push('')
L.push('**Easy Spaghetti preserved:** total sugar **14.8 g** (unchanged from v1), fiber 14.6 g, source `usda+canonical`.')
L.push('')

fs.appendFileSync(path.join(__dirname, '..', 'batch4-canonical-dryrun-v2.md'), L.join('\n') + '\n')
console.log(`Appended Regression-check section (${changed.length} changed recipes) to batch4-canonical-dryrun-v2.md`)
