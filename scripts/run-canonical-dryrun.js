#!/usr/bin/env node
/**
 * scripts/run-canonical-dryrun.js  (Batch 4)
 *
 * Drives the auth-gated /api/nutrition-canonical-dryrun route (DRY-RUN ONLY) over
 * the whole catalog, aggregates the diff, and writes batch4-canonical-dryrun.md.
 *
 * Requires `next dev` running locally (default http://localhost:3000). Mints a
 * real Firebase ID token locally (admin custom token → Identity Toolkit exchange).
 *
 * NO WRITES: the route has no apply path; this script only reads its JSON output
 * and writes one local markdown report. It never touches Firestore.
 *
 * Usage:  node scripts/run-canonical-dryrun.js [baseUrl]
 */

const fs = require('fs')
const path = require('path')
const { loadEnv, mintIdToken } = require('./_lib')

const BASE = process.argv[2] || 'http://localhost:3000'
const LIMIT = 25
const MACROS = ['calories', 'protein_g', 'carbs_g', 'fat_g', 'fiber_g', 'sugar_g']

async function postPage(token, offset) {
  const url = `${BASE}/api/nutrition-canonical-dryrun?limit=${LIMIT}&offset=${offset}`
  for (let attempt = 0; attempt < 3; attempt++) {
    try {
      const res = await fetch(url, { method: 'POST', headers: { Authorization: `Bearer ${token}` }, signal: AbortSignal.timeout(240000) })
      if (res.ok) return await res.json()
      const body = await res.text()
      throw new Error(`HTTP ${res.status}: ${body.slice(0, 200)}`)
    } catch (e) {
      if (attempt === 2) throw e
      await new Promise(r => setTimeout(r, 1500))
    }
  }
}

function fmtMacros(m) { return MACROS.map(k => `${k.replace('_g', '')} ${m[k] ?? '—'}`).join(' · ') }
function fmtDelta(d) { return MACROS.map(k => `${k.replace('_g', '')} ${d[k] > 0 ? '+' : ''}${d[k]}`).join(' · ') }

;(async () => {
  loadEnv()
  const token = await mintIdToken()
  console.log(`Minted ID token. Paging ${BASE}/api/nutrition-canonical-dryrun (limit ${LIMIT})…`)

  const diffs = []
  let meta = null
  let offset = 0
  while (true) {
    const page = await postPage(token, offset)
    if (page.error) throw new Error(`route error: ${page.error}`)
    meta = page
    diffs.push(...page.diffs)
    console.log(`  offset ${offset}: processed ${page.processed}, affected so far ~${diffs.filter(d => d.affected).length}, remaining ${page.remainingAfterBatch}`)
    if (page.remainingAfterBatch <= 0 || page.processed === 0) break
    offset += LIMIT
  }

  // ── Aggregate ──
  const ok = diffs.filter(d => !d.error)
  const errors = diffs.filter(d => d.error)
  const affected = ok.filter(d => d.affected)
  const changed = ok.filter(d => d.changed)
  const es = ok.find(d => d.recipeId === 'easy-spaghetti-with-meat-sauce')

  // biggest deltas (by absolute sugar, then calories)
  const bySugar = [...changed].sort((a, b) => Math.abs(b.canonicalDelta.sugar_g) - Math.abs(a.canonicalDelta.sugar_g)).slice(0, 20)
  const byCal = [...changed].sort((a, b) => Math.abs(b.canonicalDelta.calories) - Math.abs(a.canonicalDelta.calories)).slice(0, 20)

  // confidence shifts (old stored → proposed)
  const confShift = {}
  for (const d of ok) {
    const key = `${d.old.confidence ?? 'none'} → ${d.proposed.confidence ?? 'none'}`
    confShift[key] = (confShift[key] || 0) + 1
  }

  // Task-C: stored confidence === 'low' population
  const low = ok.filter(d => d.old.confidence === 'low')
  const lowProposed = { high: 0, medium: 0, low: 0, other: 0 }
  let lowCanonCorrected = 0
  for (const d of low) {
    const c = d.proposed.confidence
    if (c === 'high') lowProposed.high++
    else if (c === 'medium') lowProposed.medium++
    else if (c === 'low') lowProposed.low++
    else lowProposed.other++
    if (d.changed) lowCanonCorrected++
  }

  // verify log (for the table summary)
  let verify = null
  try { verify = JSON.parse(fs.readFileSync(path.join(__dirname, 'canonical-verify-log.json'), 'utf8')) } catch {}

  // ── Build report ──
  const L = []
  L.push('# Batch 4 — Canonical Staples Recompute · DRY-RUN DIFF (no writes)')
  L.push('')
  L.push('> **DRY-RUN ONLY.** Generated by `scripts/run-canonical-dryrun.js` against')
  L.push('> `/api/nutrition-canonical-dryrun` (which has **no apply path**). **Zero Firestore')
  L.push('> writes.** Stored `nutrition`, `servings`, and `confidence` are unchanged. Apply is a')
  L.push('> separate, later step after this diff is reviewed.')
  L.push('')
  L.push('## How to reproduce')
  L.push('```bash')
  L.push('npm run dev            # in one terminal')
  L.push('node scripts/run-canonical-dryrun.js     # in another (mints its own ID token)')
  L.push('# single recipe:  POST /api/nutrition-canonical-dryrun?recipeId=easy-spaghetti-with-meat-sauce')
  L.push('# low-conf only:  POST /api/nutrition-canonical-dryrun?scope=low')
  L.push('```')
  L.push('')
  L.push('## Method')
  L.push('Per recipe the engine is run twice in the same runtime:')
  L.push('- **baseline** = `computeRecipeNutrition(id, {useCanonical:false})` — the *existing* matcher.')
  L.push('- **proposed** = `computeRecipeNutrition(id, {useCanonical:true})` — the *canonical-aware* engine.')
  L.push('- **canonicalΔ** = proposed.total − baseline.total → attributable **purely** to the table.')
  L.push('- **old** = the value stored in Firestore today (reference).')
  L.push('')
  L.push('> ⚠️ Run locally **without `ANTHROPIC_API_KEY`**, so the AI fallback tier is off in *both*')
  L.push('> passes. The canonicalΔ (baseline→proposed) is therefore **exact** (same ingredients')
  L.push('> unresolved in both). Absolute totals for AI-dependent recipes can read lower than the')
  L.push('> stored `old` (which was computed on Vercel with AI), and the high/medium confidence split')
  L.push('> is a **local lower bound** (AI-off inflates `unresolved`, capping some recipes at medium).')
  L.push('> The low→not-low lift (servings recovery) and the macro corrections are AI-independent.')
  L.push('')
  L.push('## Population')
  L.push('')
  L.push('| Metric | Count |')
  L.push('|---|---|')
  L.push(`| Recipes in catalog | ${meta.catalogTotal} |`)
  L.push(`| Recomputed (this run) | ${ok.length} |`)
  L.push(`| Recompute errors (no parseable ingredients / no doc) | ${errors.length} |`)
  L.push(`| **Resolved ≥1 ingredient via canonical table** | **${affected.length}** |`)
  L.push(`| …of which a macro **materially changed** vs baseline | **${changed.length}** |`)
  if (verify) L.push(`| Canonical table entries (live-verified) | ${verify.verified}/${verify.seeds} |`)
  L.push('')

  // Easy Spaghetti
  L.push('## Target case — Easy Spaghetti With Meat Sauce')
  L.push('')
  if (es) {
    L.push(`Doc id: \`${es.recipeId}\``)
    L.push('')
    L.push('| basis | source | conf | ' + MACROS.map(k => k.replace('_g', '')).join(' | ') + ' |')
    L.push('|---|---|---|' + MACROS.map(() => '---').join('|') + '|')
    L.push(`| **old (stored)** | ${es.old.source} | ${es.old.confidence} | ` + MACROS.map(k => es.old.total[k] ?? '—').join(' | ') + ' |')
    L.push(`| baseline (canon off) | ${es.baseline.source} | ${es.baseline.confidence} | ` + MACROS.map(k => es.baseline.total[k] ?? '—').join(' | ') + ' |')
    L.push(`| **proposed (canon on)** | ${es.proposed.source} | ${es.proposed.confidence} | ` + MACROS.map(k => es.proposed.total[k] ?? '—').join(' | ') + ' |')
    L.push(`| **canonicalΔ** | | | ` + MACROS.map(k => (es.canonicalDelta[k] > 0 ? '+' : '') + es.canonicalDelta[k]).join(' | ') + ' |')
    L.push('')
    L.push(`Per-serving (proposed, ÷${es.proposed.servings}): ${fmtMacros(es.proposed.perServing)}`)
    L.push('')
    L.push('**Ingredients newly resolved via canonical table (before → after):**')
    L.push('')
    L.push('| ingredient | g | baseline match (per-100g) | canonical match (fdcId, per-100g) |')
    L.push('|---|---|---|---|')
    for (const rc of es.resolutionChanges) {
      const before = rc.before ? `${rc.before.description} — ${rc.before.calPer100g}kcal, sugar ${rc.before.sugarPer100g}, fiber ${rc.before.fiberPer100g}` : '*(unresolved in baseline)*'
      const after = `${rc.after.description} (fdc ${rc.after.fdcId}) — ${rc.after.calPer100g}kcal, sugar ${rc.after.sugarPer100g}, fiber ${rc.after.fiberPer100g}`
      L.push(`| ${rc.ingredient} | ${rc.grams} | ${before} | ${after} |`)
    }
    L.push('')
    const sOld = es.old.total.sugar_g, sNew = es.proposed.total.sugar_g
    const fOld = es.old.total.fiber_g, fNew = es.proposed.total.fiber_g
    L.push(`**Headline:** total **sugar ${sOld} → ${sNew} g**, total **fiber ${fOld} → ${fNew} g** ` +
      `(per-serving sugar ${es.old.perServing.sugar_g} → ${es.proposed.perServing.sugar_g} g). ` +
      `The implausible sugar was the mis-resolved ingredient(s) above; the canonical table routes them to the correct plain USDA entry.`)
  } else {
    L.push('_Easy Spaghetti With Meat Sauce not found in the diff set._')
  }
  L.push('')

  // Biggest deltas
  L.push('## Biggest macro changes (canonicalΔ = proposed − baseline)')
  L.push('')
  L.push('### Top sugar corrections')
  L.push('')
  L.push('| recipe | sugarΔ (g) | calΔ | proposed total (cal·sugar·fiber) | canonical ingredient(s) |')
  L.push('|---|---|---|---|---|')
  for (const d of bySugar) {
    const hits = d.canonicalHits.map(h => h.name).slice(0, 4).join(', ')
    L.push(`| ${d.title} | ${d.canonicalDelta.sugar_g > 0 ? '+' : ''}${d.canonicalDelta.sugar_g} | ${d.canonicalDelta.calories > 0 ? '+' : ''}${d.canonicalDelta.calories} | ${d.proposed.total.calories}·${d.proposed.total.sugar_g}·${d.proposed.total.fiber_g} | ${hits} |`)
  }
  L.push('')
  L.push('### Top calorie corrections')
  L.push('')
  L.push('| recipe | calΔ | sugarΔ | proposed total (cal·sugar·fiber) | canonical ingredient(s) |')
  L.push('|---|---|---|---|---|')
  for (const d of byCal) {
    const hits = d.canonicalHits.map(h => h.name).slice(0, 4).join(', ')
    L.push(`| ${d.title} | ${d.canonicalDelta.calories > 0 ? '+' : ''}${d.canonicalDelta.calories} | ${d.canonicalDelta.sugar_g > 0 ? '+' : ''}${d.canonicalDelta.sugar_g} | ${d.proposed.total.calories}·${d.proposed.total.sugar_g}·${d.proposed.total.fiber_g} | ${hits} |`)
  }
  L.push('')

  // Confidence shifts
  L.push('## Confidence shifts (old stored → proposed)')
  L.push('')
  L.push('| transition | count |')
  L.push('|---|---|')
  for (const [k, v] of Object.entries(confShift).sort((a, b) => b[1] - a[1])) L.push(`| ${k} | ${v} |`)
  L.push('')

  // Task C
  L.push('## Task C — low-confidence (49→' + low.length + ') projection against the canonical-aware engine')
  L.push('')
  L.push(`Of the **${low.length}** recipes stored \`confidence === "low"\` today, recomputed with the`)
  L.push('canonical-aware engine (still DRY-RUN):')
  L.push('')
  L.push('| proposed confidence | count |')
  L.push('|---|---|')
  L.push(`| high | ${lowProposed.high} |`)
  L.push(`| medium | ${lowProposed.medium} |`)
  L.push(`| still low | ${lowProposed.low} |`)
  if (lowProposed.other) L.push(`| other/error | ${lowProposed.other} |`)
  L.push('')
  L.push(`- **${low.length - lowProposed.low}/${low.length}** lift out of \`low\` (servings recovered on recompute — same as the Task-C re-run tool, AI-independent).`)
  L.push(`- **${lowCanonCorrected}/${low.length}** ALSO get a canonical **macro correction** the Task-C re-run alone could not make (it lifts confidence without changing the bad macro — see PRD §6). This is the added value of the canonical table.`)
  L.push(`- Per the method caveat above, the high vs medium split here is a local lower bound (AI tier off).`)
  L.push('')

  // Verify summary + excluded
  if (verify) {
    L.push('## Canonical table verification')
    L.push('')
    L.push(`- **${verify.verified}/${verify.seeds}** seeds verified live against the USDA FoodData Central API (SR Legacy / Foundation only; per-100g calories within the food-class kcal band).`)
    if (verify.failedEntries && verify.failedEntries.length) {
      L.push(`- **${verify.failedEntries.length} excluded** (left to the existing matcher — never a wrong canonical match):`)
      for (const f of verify.failedEntries) L.push(`  - \`${f.key}\` — ${f.reason}`)
    }
    L.push('')
  }

  L.push('## Confirmation')
  L.push('')
  L.push(`- **Firestore writes performed this run: ${meta.writesPerformed} (zero).** The route has no apply path.`)
  L.push('- Stored `nutrition`, `servings`, `confidence` are **unchanged**.')
  L.push('- This diff is for review only. Apply is a separate, later, explicitly-authorized step.')
  L.push('')

  const outPath = path.join(__dirname, '..', 'batch4-canonical-dryrun.md')
  fs.writeFileSync(outPath, L.join('\n') + '\n', 'utf8')

  // raw JSON alongside for spot-checking
  fs.writeFileSync(path.join(__dirname, 'canonical-dryrun-raw.json'), JSON.stringify({ meta: { ...meta, diffs: undefined }, diffs }, null, 2), 'utf8')

  console.log(`\nWrote ${outPath}`)
  console.log(`Catalog ${meta.catalogTotal} · recomputed ${ok.length} · affected ${affected.length} · changed ${changed.length} · errors ${errors.length}`)
  if (es) console.log(`Easy Spaghetti sugar(total): ${es.old.total.sugar_g} → ${es.proposed.total.sugar_g} ; fiber: ${es.old.total.fiber_g} → ${es.proposed.total.fiber_g}`)
  console.log('NO Firestore writes performed.')
  process.exit(0)
})().catch(err => { console.error('RUN ERROR:', err && err.stack || err); process.exit(1) })
